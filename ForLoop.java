package org.nn;

public class ForLoop {
	public static void main(String[] args) {
		for (int i = 0; i <= 10; i++) {
			if (i % 2 == 0) {
//	if we continue; statement it skips the condition and prints the remaining values
				//Continue is used to print "Odd no." 
			}
			System.out.println();
		}

	}
}
