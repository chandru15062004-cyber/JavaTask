package org.statement;

public class ElseIf {
	// "else if" condition-> it is used to create multiple conditions4
	public static void main(String[] args) {

		int i = 56;
		if (i <= 55) {
			System.out.println("Yes");
		} else if (i == 55) {
			System.out.println("No");
		} else if (i % 2 == 0) {
			System.out.println("GG");
		}
	}
}