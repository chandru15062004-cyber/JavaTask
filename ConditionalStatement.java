package org.statement;

//"if else" Condition->if 'if' condition is true it executes the if block 
//(or) if the condition is false then it checks the 'else' block if its true then 'else' block will prints
public class ConditionalStatement {
	public static void main(String[] args) {
		// Initialization
		int i = 46456;
		if (i < 15) {
			System.out.println("yes");
		} else {
			System.out.println("no");
		}
	}
}
